#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while ((char)c != s[i] && s[i] != '\0')
		i++;
	if (((char)c) == s[i])
		return (&(((char *)s)[i]));
	else
		return (NULL);
}
